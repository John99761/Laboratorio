# Solicitar al usuario dos números
dividendo = float(input("Introduce el dividendo: "))
divisor = float(input("Introduce el divisor: "))

# Calcular el residuo de la división
residuo = dividendo % divisor

# Mostrar el residuo de la división
print(f"El residuo de la división entre {dividendo} y {divisor} es: {residuo}")
